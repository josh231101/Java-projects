    private void comboPaisesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboPaisesItemStateChanged
        
        if(evt.getStateChange() == ItemEvent.SELECTED){
            Pais pais =(Pais) comboPaises.getSelectedItem();
            
            DefaultTableModel modeloTabla = new DefaultTableModel();
            tablaEstados.setModel(modeloTabla);
        
            
        
            PreparedStatement ps = null;
            ResultSet rs = null;
        
        try{
            Conexion con = new Conexion();
            java.sql.Connection conexion = con.getConnection();
            
            ps = (PreparedStatement) conexion.prepareStatement("select idEstado,nombreEstado from estados where idPais=" + pais.getIdPais());
            rs = ps.executeQuery();
            
            modeloTabla.addColumn("ID Estado");
            modeloTabla.addColumn("Nombre");
            
            //OBTENES CUANTAS FILAS HAY
            ResultSetMetaData rsMD = rs.getMetaData();
            int cantidadColumnas = rsMD.getColumnCount();
            
            int anchos[] = {50,150};
            
            for(int i=0;i<cantidadColumnas;i++){
                tablaEstados.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }
            
           //MIENTRAS SIGA HABIENDO DATOS EN LA BD
            while(rs.next()){
                Object fila[] = new Object[cantidadColumnas];
                for(int i =0;i<cantidadColumnas;i++){
                    fila[i] = rs.getObject(i+1);
                }
                modeloTabla.addRow(fila);
            }
            
        }   catch(Exception e){
            System.err.println("Error null pointer: " +e );
            }
        }
        
        
    }//GEN-LAST:event_comboPaisesItemStateChanged

