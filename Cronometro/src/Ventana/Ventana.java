    private void botonPausarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPausarActionPerformed
        // TODO add your handling code here:
        
        tiempo.stop();
        botonInicar.setEnabled(true);
        botonPausar.setEnabled(false);
    }//GEN-LAST:event_botonPausarActionPerformed

    private void botonInicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInicarActionPerformed
        tiempo.start();
        botonInicar.setEnabled(false);
        botonInicar.setText("Reanudar");
        botonPausar.setEnabled(true);
        botonDetener.setEnabled(true);
        
        
    }//GEN-LAST:event_botonInicarActionPerformed

    private void botonDetenerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonDetenerActionPerformed
        // TODO add your handling code here:
        if(tiempo.isRunning()){//Si el tiempo sigue corriendo
            tiempo.stop();
            botonInicar.setEnabled(true);
            
            
        }
        botonInicar.setText("Iniciar");
        botonPausar.setEnabled(false);
        
        botonDetener.setEnabled(false);
        
        horas = 0;
        minutos= 0;
        segundos = 0;
        centesimaSegundo = 0;
        
        actualizarEtiquetaTiempo();
        
    }//GEN-LAST:event_botonDetenerActionPerformed

