    private void botonGuardarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarDatosActionPerformed
        
        Archivo archivo = new Archivo();
        archivo.crearArchivo();
        
        String nombre,correo,celular;
        //Obtenemos los datos de las cajas de tetxo
        nombre = cajaNombre.getText();
        correo = cajaCorreo.getText();
        celular = cajaTelefono.getText();
        //Creamos objeto persona
        Persona persona = new Persona(nombre, correo, celular);
        //EsCRIBIMOS LOS DATOS DE LA PERSONA EN EL ARCHIVO
        archivo.escribirTexto(persona);
        
        cajaNombre.setText("");
        cajaCorreo.setText("");
        cajaTelefono.setText("");
        
    }//GEN-LAST:event_botonGuardarDatosActionPerformed

